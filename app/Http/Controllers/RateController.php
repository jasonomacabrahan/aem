    public function store(Request $request)
    {
        $validated = $request->validate([
            'activityid' => 'required',
            'rate' => 'required|string|max:255',
        ]);
        $t = new Rate;
        $t->activity_id= $request->activityid;
        $t->rate= $request->rate;
        $t->created_at= NOW();
        $t->updated_at= NOW();
        $t->save();
        return redirect()->route('feedback.index');
    }

